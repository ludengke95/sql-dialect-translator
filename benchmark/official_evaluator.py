#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
=============================================================================
SDT - PARROT Official Evaluation Adapter & Scoring Runner
=============================================================================
适配 weAIDB/PARROT 官方 ANTLR Parsers 对 SDT 翻译导出的 SQL 进行语法校验与打分
=============================================================================
"""

import argparse
import csv
import json
import os
import re
import sys


def parse_args():
    parser = argparse.ArgumentParser(description="PARROT Official Evaluator Adapter for SDT")
    parser.add_argument("--dataset", required=True, help="Path to original PARROT dataset JSON")
    parser.add_argument("--predictions", required=True, help="Path to sdt_predictions.json generated by SDT Runner")
    parser.add_argument("--csv", help="Path to sdt_predictions.csv to update with syntaxPass column")
    parser.add_argument("--evaluator-dir", default="benchmark/parrot-evaluator", help="Path to cloned weAIDB/PARROT repo")
    return parser.parse_args()


def check_syntax_antlr(sql, dialect, evaluator_dir):
    """
    尝试加载 weAIDB/PARROT 官方 ANTLR Parser 对翻译后的 SQL 进行语法匹配
    """
    if not sql or not sql.strip():
        return False, "Empty SQL"

    evaluator_path = os.path.abspath(evaluator_dir)
    if evaluator_path not in sys.path:
        sys.path.insert(0, evaluator_path)

    dialect_upper = dialect.upper() if dialect else ""

    try:
        from antlr4 import CommonTokenStream, InputStream

        if dialect_upper in ["PG", "POSTGRES", "POSTGRESQL"]:
            from validator.pg_parser.PostgreSQLLexer import PostgreSQLLexer
            from validator.pg_parser.PostgreSQLParser import PostgreSQLParser

            lexer = PostgreSQLLexer(InputStream(sql))
            stream = CommonTokenStream(lexer)
            parser = PostgreSQLParser(stream)
            parser.removeErrorListeners()
            parser.root()
            return parser.getNumberOfSyntaxErrors() == 0, f"Syntax errors: {parser.getNumberOfSyntaxErrors()}"

        elif dialect_upper in ["MY", "MYSQL"]:
            from validator.mysql_parser.MySqlLexer import MySqlLexer
            from validator.mysql_parser.MySqlParser import MySqlParser

            lexer = MySqlLexer(InputStream(sql))
            stream = CommonTokenStream(lexer)
            parser = MySqlParser(stream)
            parser.removeErrorListeners()
            parser.root()
            return parser.getNumberOfSyntaxErrors() == 0, f"Syntax errors: {parser.getNumberOfSyntaxErrors()}"

        elif dialect_upper in ["ORACLE"]:
            from validator.oracle_parser.PlSqlLexer import PlSqlLexer
            from validator.oracle_parser.PlSqlParser import PlSqlParser

            lexer = PlSqlLexer(InputStream(sql))
            stream = CommonTokenStream(lexer)
            parser = PlSqlParser(stream)
            parser.removeErrorListeners()
            parser.sql_script()
            return parser.getNumberOfSyntaxErrors() == 0, f"Syntax errors: {parser.getNumberOfSyntaxErrors()}"

        elif dialect_upper in ["TSQL", "SQLSERVER"]:
            from validator.tsql_parser.TSqlLexer import TSqlLexer
            from validator.tsql_parser.TSqlParser import TSqlParser

            lexer = TSqlLexer(InputStream(sql))
            stream = CommonTokenStream(lexer)
            parser = TSqlParser(stream)
            parser.removeErrorListeners()
            parser.tsql_file()
            return parser.getNumberOfSyntaxErrors() == 0, f"Syntax errors: {parser.getNumberOfSyntaxErrors()}"

    except Exception:
        return check_syntax_sqlglot(sql, dialect)

    return check_syntax_sqlglot(sql, dialect)


def check_syntax_sqlglot(sql, dialect):
    """
    Fallback 逻辑：利用 sqlglot 库进行 SQL 语法验证
    """
    try:
        import sqlglot

        dialect_map = {
            "POSTGRESQL": "postgres",
            "POSTGRES": "postgres",
            "MYSQL": "mysql",
            "ORACLE": "oracle",
            "SQLSERVER": "tsql",
            "TSQL": "tsql",
        }
        target_dialect = dialect_map.get(dialect.upper(), "postgres")
        sqlglot.parse_one(sql, read=target_dialect)
        return True, "Valid SQL"
    except Exception as e:
        err_msg = str(e).replace("\n", " ")
        if len(err_msg) > 60:
            err_msg = err_msg[:57] + "..."
        return False, err_msg


def load_json_safe(filepath):
    """
    容错地加载 JSON 文件，自动处理非严格控制字符（Invalid control character）
    """
    with open(filepath, "r", encoding="utf-8", errors="replace") as f:
        content = f.read()

    try:
        return json.loads(content, strict=False)
    except Exception:
        # 清除无法直接在 JSON 字符串中出现的不合法控制字符（保留换行 \n \r \t）
        clean_content = re.sub(r'[\x00-\x08\x0b\x0c\x0e-\x1f]', '', content)
        return json.loads(clean_content, strict=False)


def export_csv_with_syntax_pass(predictions, csv_path):
    """
    更新导出包含 syntaxPass 字段的 CSV 文件（7 列，带 UTF-8 BOM）
    """
    if not csv_path:
        return

    os.makedirs(os.path.dirname(os.path.abspath(csv_path)), exist_ok=True)
    with open(csv_path, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.writer(f, quoting=csv.QUOTE_ALL)
        writer.writerow(["id", "sourceDialect", "targetDialect", "sourceSql", "translatedSql", "success", "syntaxPass"])
        for item in predictions:
            writer.writerow([
                item.get("id", ""),
                item.get("sourceDialect", ""),
                item.get("targetDialect", ""),
                item.get("sourceSql", ""),
                item.get("translatedSql", ""),
                "true" if item.get("success", False) else "false",
                "true" if item.get("syntaxPass", False) else "false"
            ])


def main():
    args = parse_args()

    if not os.path.exists(args.predictions):
        print(f"Error: Predictions file not found: {args.predictions}")
        sys.exit(1)

    predictions = load_json_safe(args.predictions)

    total_count = len(predictions)
    sdt_success_count = 0
    syntax_pass_count = 0

    direction_stats = {}

    for item in predictions:
        src_dialect = item.get("sourceDialect", "UNKNOWN").upper()
        target_dialect = item.get("targetDialect", "UNKNOWN").upper()
        direction = f"{src_dialect} -> {target_dialect}"

        if direction not in direction_stats:
            direction_stats[direction] = {
                "total": 0,
                "sdt_success": 0,
                "syntax_pass": 0,
            }

        stats = direction_stats[direction]
        stats["total"] += 1

        is_sdt_success = item.get("success", False)
        translated_sql = item.get("translatedSql", "")

        if is_sdt_success:
            sdt_success_count += 1
            stats["sdt_success"] += 1

        # 语法校验
        is_syntax_valid, reason = check_syntax_antlr(translated_sql, target_dialect, args.evaluator_dir)
        syntax_pass = bool(is_sdt_success and is_syntax_valid)
        item["syntaxPass"] = syntax_pass

        if syntax_pass:
            syntax_pass_count += 1
            stats["syntax_pass"] += 1

    # 导出更新包含 syntaxPass 列的 CSV
    csv_path = args.csv
    if not csv_path and args.predictions.endswith(".json"):
        csv_path = args.predictions[:-5] + ".csv"
    if csv_path:
        export_csv_with_syntax_pass(predictions, csv_path)

    # 输出 Markdown 格式的评测报告
    print("# 🏆 PARROT Official Evaluator 打分报告\n")
    print(f"- **评测用例总数**: {total_count}")
    print(f"- **SDT 引擎转换率**: {sdt_success_count} / {total_count} ({(sdt_success_count*100.0/total_count if total_count > 0 else 0.0):.2f}%)")
    print(f"- **官方语法验证通过数 (Syntax Pass)**: {syntax_pass_count} / {total_count} ({(syntax_pass_count*100.0/total_count if total_count > 0 else 0.0):.2f}%)\n")

    print("### 📊 方言方向打分明细\n")
    print("| 方言转换方向 | 评测用例数 | SDT 转换成功率 | ANTLR 语法通过率 (Syntax Pass) |")
    print("| --- | --- | --- | --- |")

    for dir_name, stats in direction_stats.items():
        cnt = stats["total"]
        sdt_rate = (stats["sdt_success"] * 100.0 / cnt) if cnt > 0 else 0.0
        syntax_rate = (stats["syntax_pass"] * 100.0 / cnt) if cnt > 0 else 0.0
        print(f"| {dir_name} | {cnt} | {stats['sdt_success']} ({sdt_rate:.2f}%) | {stats['syntax_pass']} ({syntax_rate:.2f}%) |")

    overall_sdt_rate = (sdt_success_count * 100.0 / total_count) if total_count > 0 else 0.0
    overall_syntax_rate = (syntax_pass_count * 100.0 / total_count) if total_count > 0 else 0.0
    print(f"| **全量汇总 (TOTAL)** | **{total_count}** | **{sdt_success_count} ({overall_sdt_rate:.2f}%)** | **{syntax_pass_count} ({overall_syntax_rate:.2f}%)** |\n")


if __name__ == "__main__":
    main()
